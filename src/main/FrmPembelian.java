    private void btnSelesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelesaiActionPerformed
        // TODO add your handling code here:
        try{
            String faktur = txtTransaksi.getText();
            fTanggal();
            String manajer = cmbManajer.getSelectedItem().toString();
            String namamnjr = txtManajer.getText();
            String supplier = cmbSupplier.getSelectedItem().toString();
            String namasplr = txtSupplier.getText();
            String htotal = txtTotal.getText();
            SQL = "INSERT INTO pembelian VALUES('"+faktur+"', "
                    + "'"+tanggal+"', "
                    + "'"+manajer+"', "
                    + "'"+namamnjr+"', "
                    + "'"+supplier+"', "
                    + "'"+namasplr+"', "
                    + "'"+htotal+"')";
            Connection conn = koneksi.getConnection();
            PreparedStatement pst = conn.prepareStatement(SQL);
            pst.execute();

            int t = tablePembelian.getRowCount();
            for(int i=0; i < t ; i++) {
                String kode = tablePembelian.getValueAt(i,0).toString();
                String barang = tablePembelian.getValueAt(i,1).toString();
                String jenis = tablePembelian.getValueAt(i,2).toString();
                String harga = tablePembelian.getValueAt(i,3).toString();
                int jumlah = Integer.parseInt(tablePembelian.getValueAt(i,4).toString());
                SQL = "INSERT INTO barang VALUES('"+faktur+"', "
                        + "'"+kode+"', "
                        + "'"+barang+"', "
                        + "'"+jenis+"', "
                        + "'"+harga+"', "
                        + "'"+jumlah+"')";
                PreparedStatement pst2 = conn.prepareStatement(SQL);
                pst2.execute();
            }
            JOptionPane.showMessageDialog(null,"Pembelian berhasil disimpan");
            bersih();
            tablePembelian.removeAll();
        } catch(SQLException e) {
            System.out.println("Error : "+e);
            JOptionPane.showMessageDialog(null,"No Jual Sudah ada");
        }
    }//GEN-LAST:event_btnSelesaiActionPerformed

