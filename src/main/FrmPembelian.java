    private void hitungJual() {
        int xtot,xhrg;
        int xjml;
        xhrg = Integer.parseInt(txtHarga.getText());
        xjml = Integer.parseInt(txtJumlah.getText());
        xtot = xhrg*xjml;
        String xtotal = Integer.toString(xtot);
        txtTotal.setText(xtotal);
        total= total+ xtot;
        txtBayar.setText(Integer.toString(total));
    }
    
    private void tambahBrg() {
        try {
            tablePembelian.setModel(model);
            String tKode = txtKode.getText();
            String tNama = cmbBarang.getSelectedItem().toString();
            String tJenis = txtJenis.getText();
            int hrg = Integer.parseInt(txtHarga.getText());
            int jml = Integer.parseInt(txtJumlah.getText());
            int tot = Integer.parseInt(txtTotal.getText());
            model.addRow(new Object[]{tKode,tNama,tJenis,hrg,jml,tot});
        } catch (Exception e) {
            System.out.println("Error : "+e);
        }
    }
    
    private void fTanggal() {
        String DATE_FORMAT = "dd-mm-yyyy";
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat(DATE_FORMAT);
        Calendar c1 = Calendar.getInstance();
        int year = c1.get(Calendar.YEAR);
        int month = c1.get(Calendar.MONTH)+1;
        int day = c1.get(Calendar.DAY_OF_MONTH);
        tanggal = Integer.toString(year)+"-"+Integer.toString(month)+"-"+Integer.toString(day);
    }
    
