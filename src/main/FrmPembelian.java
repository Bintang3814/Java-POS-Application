    private void kodeManajer() {
        cmbManajer.removeAllItems();
        Connection conn = koneksi.getConnection();
        try {
            SQL = "SELECT id FROM karyawan WHERE id LIKE '%M%'";
            java.sql.Statement cs = conn.createStatement();
            java.sql.ResultSet rest = cs.executeQuery(SQL);
            while ((rest.next())) {
                cmbManajer.addItem(rest.getString(1));
            }
        } catch (SQLException e) {
            
        }
    }
    
