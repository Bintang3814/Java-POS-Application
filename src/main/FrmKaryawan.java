    private void txtCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCariActionPerformed
        // TODO add your handling code here:
        Connection conn = koneksi.getConnection();
        int r = tableKaryawan.getRowCount();
        while (r-- > 0) {
           model.removeRow(r);
        }
            try {
            java.sql.Statement smt = conn.createStatement();
            if (cmbCari.getSelectedItem().equals("ID")) {
                SQL = "SELECT * FROM karyawan WHERE id = '"+txtCari.getText()+"'";
                System.out.println(SQL);
            } else {
                SQL = "SELECT * FROM karyawan WHERE nama like '%"+txtCari.getText()+"%'";
                System.out.println(SQL);
            }
         java.sql.ResultSet rest = smt.executeQuery(SQL);
         while (rest.next()) {
         model.addRow(new Object[] {
               rest.getString("id"),
               rest.getString("nama"),
               rest.getString("umur"),
               rest.getString("noTelp"),
               rest.getString("alamat"),
               rest.getString("kota"),
               rest.getString("provinsi"),
               rest.getString("kodeDtm"),
               rest.getString("kodeJbtn"),
               rest.getString("stat")
            });
         }
       } catch (Exception e) {
         System.out.println(e.getMessage());
       }
    }//GEN-LAST:event_txtCariActionPerformed

