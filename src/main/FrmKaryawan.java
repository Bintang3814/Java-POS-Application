    private void tampilData() {
        model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }
        };
        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("Umur");
        model.addColumn("No. Telp");
        model.addColumn("Alamat");
        model.addColumn("Kota");
        model.addColumn("Provinsi");
        model.addColumn("Kode Departemen");
        model.addColumn("Kode Jabatan");
        model.addColumn("Status");
        tableKaryawan.setModel(model);
        Connection conn = koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "SELECT * FROM karyawan";
            java.sql.ResultSet rest = stmt.executeQuery(SQL);
            while (rest.next()) {
                model.addRow(new Object[] {
                    rest.getString("id"),
                    rest.getString("nama"),
                    rest.getString("umur"),
                    rest.getString("noTelp"),
                    rest.getString("alamat"),
                    rest.getString("kota"),
                    rest.getString("provinsi"),
                    rest.getString("kodeDtm"),
                    rest.getString("kodeJbtn"),
                    rest.getString("stat")
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
